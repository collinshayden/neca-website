  <h2>New England Amateur Team Championship</h2>
  <div class="body">
    <h3>Motivation</h3>
    <p>
      The New England Amateur Team Championship will be an annual 4 player team
      event following the format of the US Amateur Team tournaments. Currently
      there is a complete lack of team tournaments in New England. State
      scholastic tournaments usually have a team category, however it is not a
      purely team oriented event. Our goal is to popularize team play within New
      England, as it is a very fun and entertaining format to play chess.
      Playing on a team also encourages fighting for every point possible, and
      often results in very exciting rounds.
    </p>
    <p>
      The rules of this event are based on the
      <a
        href="https://new.uschess.org/sites/default/files/media/documents/2021-22-panam-collegechessrules.pdf"
        target="_blank"
        >Pan-American Intercollegiate Chess Team Championships rules</a
      >.
    </p>
