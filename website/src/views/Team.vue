      <Tournament
        title="1st New England Amateur Team Championship - March 23/24"
        format="4SS G90+30, U1800 (avg), EF $200/team, Prizes $2000"
        link_desc="Venue TBD"
      />
    </div>

    <h3>Rules</h3>
    <ol>
      <li>Format & Tournament Rules</li>
      <ol type="A">
        <li>Tournament Format and Rules</li>
        <ol type="a">
          <li>
            The preferred schedule is a four round, two day event. Two rounds
            will be played each day.
          </li>
          <li>The tournament will be USCF Regular rated.</li>
          <li>
            The event is a Swiss System, paired strictly team vs. team. Board 1
            of Team A plays Board 1 of Team B, through board four. (See Rule 2C,
            2G for allowable substitutions using alternates).
          </li>
          <li>
            For pairings, the TD/Arbiter uses each team’s average rating as
            described in Rule 2B. Each team’s average rating is used for Wall
            Chart ranking and class-prize eligibility.
          </li>
          <li>
            A team gets one match point if the combined scores of the four
            players in a round is 2.5 points or more, one-half match point if
            the combined score is exactly 2.0, and zero match points if the
            combined score is 1.5 or less. Teams are grouped by their match
            points and then ranked within the group by their average team
            ratings. Byes, defaults, lateness, and so forth are treated in the
            same manner as in individual tournaments.
          </li>
          <li>
            Each team’s total match points, not total game points, determines
            their final standings.
          </li>
          <li>
            A team assigned "White" has white on Boards 1 and 3, black on Boards
            2 and 4. Conversely, a team assigned "Black" has black on Boards 1
            and 3, white on Boards 2 and 4.
          </li>
          <li>
            The rule regarding same color in a series (e.g., "the three blacks
            in a row rule") is less important in team play because each team has
            two whites and two blacks in every round.
          </li>
          <li>
            The time control for all rounds is G/90 with 30 seconds of time
            increment from move one. Each player must keep chess notation,
            move-by-move, throughout the entire game.
          </li>
          <li>
            Late arrivals for a game have 1 hour from the scheduled start time
            of the round before their game shall be forfeited—not one hour from
            the time the round started.
          </li>
        </ol>
        <li>Entry fees and Registration</li>
        <ol type="a">
          <li>
            The advance entry fee for a team will be determined by the
            organizer. The late entry fee (or on-site/door entry fee) shall not
            be more than 25% above the advance entry fee.
          </li>
          <li>
            The deadline for advance entries should be two to three weeks before
            the event (possibly to coincide with the date by which hotel
            reservations should be made).
          </li>
          <li>
            Late entry will be open until 1 hour before the start of Round 1.
            Any entries after this time shall be assigned a half-point bye by
            the Chief TD / Arbiter (e.g., 0.5 match points for the team). Teams
            registering on-site must have previously provided proof of
            eligibility and received approval from the eligibility committee.
          </li>
        </ol>
      </ol>
      <li>Team Requirements</li>
      <ol type="A">
        <li>
          A team is made up of four players plus up to two optional alternates.
        </li>
        <li>The rating average of a team must not exceed 1800.</li>
        <ol type="a">
          <li>Official regular ratings will be used for these calculations.</li>
          <li>
            All lineups for a given round must fall below the 1800 average
            rating cap. Alternates that are not playing in a given round are not
            included in the rating average.
          </li>
          <li>Unrated players will be counted as having a rating of 1100.</li>
        </ol>
        <li>
          At the start of the tournament, teams are ranked-ordered and sequenced
          according to the average rating of the four highest-rated players
          listed on the team’s roster provided during the registration process.
          The team with the highest average rating is the top seed, etc. A
          team’s average rating is a fixed value for the duration of the
          tournament, it is not re-calculated by round based on a team’s line-up
          for a round.
        </li>
        <li>
          A team may decide to use one or both of its alternates in a round. Any
          regular team member may sit out when an alternate plays; other team
          members move up team’s line-up accordingly.
        </li>
        <li>
          Teams must order their players in descending rating order for each
          round, except that 50-point transpositions are allowed. That list of
          players for a specific round is known as the team’s “Lineup.” The
          Lineup is not to be confused with the team’s official “Roster”, which
          is submitted as part of the registration process and lists all primary
          players and up to 2 alternates in descending rating sequence.
        </li>
        <li>Unrated players must be placed below rated players.</li>
        <li>
          A team must have a minimum of three players to compete for prizes. If
          only two players arrive from a team before round 1, they will be
          allowed to compete provided their teammates are expected to arrive. If
          these teammates do not arrive, then the two who did arrive are
          ineligible for all prizes.
        </li>
        <li>
          All teams shall designate a Team Captain. If a team does not designate
          a Team Captain during Registration, then the Chief TD/ Arbiter can
          automatically designate the team’s highest rated player as the
          captain. The role of the Team Captain includes the following
          functions:
        </li>
        <ol type="a">
          <li>Check player registrations and wall charts for accuracy.</li>
          <li>
            Turn in the Team Lineup for the next round to the Chief TD / Arbiter
            at least one hour before the round’s scheduled start time.
          </li>
          <li>Ensure the team arrives on time for each scheduled round.</li>
          <li>
            Ensure the team members play in correct board order as shown on the
            Team Lineup.
          </li>
          <li>Report game results to the Chief TD / Arbiter.</li>
          <li>Ensure game results and standings are accurate.</li>
          <li>
            Team Captains are not allowed to communicate with players while
            their games are in progress.
          </li>
        </ol>
        <li>
          "Package deals" such as offering draws on boards 1 and 4 are not
          permitted. Any pre-arranged results will result in disciplinary action
          up to and including forfeiture of the current round, disqualification
          for prizes, or disqualification from the remainder of the tournament.
        </li>
      </ol>
      <li>Conduct of the Tournament</li>
      <ol type="A">
        <li>
          A team may not change its official roster after the end of the
          registration period without the approval of the Chief TD/ Arbiter.
        </li>
        <li>
          If, during a round, players were placed out of order due to the TD’s
          error, then the proper order will be re-established for the next
          round. When this occurs the previous results will stand for the team
          and for individual games.
        </li>
        <li>
          If, during a round, players were placed or played out of order due to
          an error by the team, then the team may face disciplinary actions as
          determined by the Chief TD / Arbiter up to and including forfeiture of
          one or more board results and disqualification from the remainder of
          the tournament.
        </li>
        <li>
          Announcements by the organizer or Chief TD/Arbiter shall be made
          before the start of each round.
        </li>
      </ol>
      <li>Electronic Devices</li>
      <ol type="A">
        <li>
          No electronic devices shall be in the possession of a player,
          regardless of the player’s location, while that player’s game is in
          progress.
        </li>
        <li>
          Prohibited devices include, but are not limited to, cell or smart
          phones, tablets, computers, and Apple or Android watches.
        </li>
        <li>
          Violation of these electronic device rules by a player or member of
          their team may result in the immediate forfeiture of game(s), eviction
          from the tournament, and/or banishment from the tournament playing
          area.
        </li>
      </ol>
      <li>Prizes</li>
      <ol type="A">
        <li>Determination & Eligibility</li>
        <ol type="a">
          <li>
            Team prizes will be distributed based on match points, and
            individual prizes will be distributed on board points. If multiple
            teams/players tie for the same prize category, the prize(s) will be
            summed and split as is dictated in the US Chess Rules.
          </li>
          <li>
            To be eligible for prizes, 3/4 of a team's lineup in each round must
            be residents of New England. Determination of residence will be
            taken from the US Chess website, unless other information is
            provided to the administrator.
          </li>
        </ol>
        <li>Prize Categories</li>
        <ol type="a">
          <li>
            The top finishing team overall will receive a prize in proportion to
            25% of the prize fund.
          </li>
          <li>
            The second overall team will receive a prize in proportion to 20% of
            the prize fund.
          </li>
          <li>
            The top team with an average team rating under 1600 will receive a
            prize in proportion to 15% of the prize fund.
          </li>
          <li>
            The highest scoring player on each board number will recieve a prize
            in proportion to 10% of the prize fund.
          </li>
        </ol>
      </ol>
    </ol>
